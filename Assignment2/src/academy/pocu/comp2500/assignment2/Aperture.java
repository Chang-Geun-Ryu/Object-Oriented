package academy.pocu.comp2500.assignment2;

public class Aperture {
    private String text;
    private int x;
    private int y;
    private int width;
    private int height;

    public Aperture(String text, int x, int y, int width, int height) {
        this.text = text;
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
